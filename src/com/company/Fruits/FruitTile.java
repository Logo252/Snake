package com.company.Fruits;

/**
 * Class FruitTile is responsible for managing all kind of fruits on the board.
 */
public enum FruitTile {

    /**
     * Normal fruit.
     */
    NORMAL_FRUIT,

    /**
     * Fruit with more speed.
     */
    MORE_SPEED_FRUIT,

    /**
     * Fruit with less speed.
     */
    LESS_SPEED_FRUIT,

    /**
     * Fruit with more score.
     */
    MORE_SCORE_FRUIT,

    /**
     * Fruit with less score.
     */
    LESS_SCORE_FRUIT,

}
