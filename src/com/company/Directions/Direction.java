package com.company.Directions;

/**
 * Enum Direction is used to determine which way the snake is moving.
 */
public enum Direction {

    /**
     * Moving north (Up).
     */
    UP,

    /**
     * Moving east (Right).
     */
    RIGHT,

    /**
     * Moving south (Down).
     */
    DOWN,

    /**
     * Moving west (Left).
     */
    LEFT

}
