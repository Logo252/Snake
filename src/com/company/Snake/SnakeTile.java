package com.company.Snake;

/**
 * Class SnakeTile is responsible for snake's head and body on the board.
 */
public enum SnakeTile {

    /**
     * Snake's head, which will be different than snake's body.
     */
    SNAKE_HEAD,

    /**
     * Snake's body, which will grow if snake eats fruit.
     */
    SNAKE_BODY

}
